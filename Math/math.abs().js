/*
Math.abs() method
the abs here means absolute
absolute: returns a number a a whole number but converts it to positive number
*/
let  a = 10; b = 20;
console.log(a-b); //returns negative
console.log(b-a); // returns positive
console.log(Math.abs(b-a)); // should have returns negative if we didn's use abs method...



/////////////////////////////////////   try them out    //////////////////////////////////////
console.log(Math.abs("-1")); // 1
console.log(Math.abs(-2)); // 2
console.log(Math.abs(null)); // 0
console.log(Math.abs("")); // 0
console.log(Math.abs([])); // 0
console.log(Math.abs([2])); // 2
console.log(Math.abs([1, 2])); // NaN
console.log(Math.abs({})); // NaN
console.log(Math.abs("string")); // NaN
console.log(Math.abs()); // NaN



